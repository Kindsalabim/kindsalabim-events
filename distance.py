def haversine(lat1: float, lon1: float, lat2: float, lon2: float) -> float:
    """Entfernung in km zwischen zwei Koordinaten."""
    R = 6371
    dlat = math.radians(lat2 - lat1)
    dlon = math.radians(lon2 - lon1)
    a = math.sin(dlat/2)**2 + math.cos(math.radians(lat1)) * math.cos(math.radians(lat2)) * math.sin(dlon/2)**2
    return R * 2 * math.asin(math.sqrt(a))

def get_coords_for_plz(plz: str):
    return PLZ_COORDS.get(plz[:5])

def get_coords_for_address(address: str):
    """Versucht PLZ aus Adresse zu extrahieren und Koordinaten zu liefern."""
    import re
    match = re.search(r'\b(\d{5})\b', address)
    if match:
        return get_coords_for_plz(match.group(1))
    return None

def rank_contractors(contractors, event_address: str):
    """
    Rankt Dienstleister nach:
    1. Entfernung vom Veranstaltungsort (aufsteigend)
    2. Erfahrungspunkte (absteigend)
    3. Mobilität: Auto=3, Beides=2, ÖPNV=1
    """
    event_coords = get_coords_for_address(event_address)

    def score(d):
        # Entfernung
        dist = 9999
        if event_coords:
            c_coords = get_coords_for_plz(d.plz or "")
            if c_coords:
                dist = haversine(event_coords[0], event_coords[1], c_coords[0], c_coords[1])

        mob_score = {"Auto": 3, "Beides": 2, "ÖPNV": 1}.get(d.mobilitaet, 1)
        # Sortierung: erst Entfernung (aufsteigend), dann Erfahrung (absteigend), dann Mobilität
        return (dist, -d.erfahrungspunkte, -mob_score)

    return sorted(contractors, key=score)
